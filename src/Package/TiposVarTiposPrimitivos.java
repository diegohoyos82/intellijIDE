package Package;

public class TiposVarTiposPrimitivos {
    public static void main(String[] args){
        var numeroEntero = 10;
        System.out.println("Numero Entero:" + numeroEntero);
        var numeroDouble = 10.0;
        System.out.println("Numero Doble:" + numeroDouble);
        var numeroFloat = 10.F;
        System.out.println("Numero Flotante:" + numeroFloat);
    }
}
